"""Calculator library containing basic math operations."""


def add(first_term, second_term):
    """Takes in two numbers first_term and second_term, returns the sum of two"""
    return first_term + second_term


def subtract(first_term, second_term):
    """Takes in two numbers first_term and second_term, returns the substract of two"""
    return first_term - second_term


def multiply(first_term, second_term):
    """Takes in two numbers first_term and second_term, returns the product of two"""
    return first_term * second_term
